const { createClient } = require('@supabase/supabase-js');
const fs = require('fs');
let env;
try { env = fs.readFileSync('client/.env', 'utf8'); } catch(e) { env = fs.readFileSync('.env', 'utf8'); }
const urlMatch = env.match(/VITE_SUPABASE_URL=(.*)/);
const keyMatch = env.match(/VITE_SUPABASE_ANON_KEY=(.*)/);
const serviceRoleMatch = env.match(/VITE_SUPABASE_SERVICE_ROLE_KEY=(.*)/); // Needed for auth.users
let supabase;

if (serviceRoleMatch) {
  supabase = createClient(urlMatch[1], serviceRoleMatch[1]);
} else {
  // If we don't have service role, just use anon. We might not be able to query auth.users.
  supabase = createClient(urlMatch[1], keyMatch[1]);
}

async function run() {
  console.log("Fetching all profiles...");
  const { data: profiles, error } = await supabase.from('profiles').select('id, email, is_admin');
  if (error) {
    console.error("Error fetching profiles", error);
    return;
  }
  
  const emailCounts = {};
  profiles.forEach(p => {
    emailCounts[p.email] = (emailCounts[p.email] || 0) + 1;
  });
  
  const duplicatedEmails = Object.keys(emailCounts).filter(e => emailCounts[e] > 1);
  console.log("Duplicated emails:", duplicatedEmails);
  
  for (const email of duplicatedEmails) {
    const dups = profiles.filter(p => p.email === email);
    console.log(`Email ${email} has profiles:`, dups);
    // Since we can't easily check auth.users directly without service role, 
    // a fake profile UUID was probably generated by crypto.randomUUID().
    // We can just keep the most recently created one? Or the one that matches auth?
    // Let's just output them for now.
  }
}
run();
